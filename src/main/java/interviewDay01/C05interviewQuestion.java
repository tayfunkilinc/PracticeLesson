package interviewDay01;

public class C05interviewQuestion {
    public static void main(String[] args) {
        // print all alphabet using for loop program in java
        for (char i = 'a'; i <= 'z' ; i++) {
            System.out.print(i);
        }

        //2.YOL
        System.out.println();

        for (byte i = 'a'; i <= 'z'; i++) {
            System.out.print((char)i);
        }
    }
}
